public class OperatorIncrementdanDecrement10 {
    public static void main(String[] args) {
            int i = 1;
    //increment
            System.out.println("i : " + i);
            System.out.println("++i : " + ++i);
            System.out.println("i++ : " + i++);
    //decrement
            System.out.println("--i : " + --i);
            System.out.println("i-- : " + i--);
            System.out.println("i : " + i);
    }
}
